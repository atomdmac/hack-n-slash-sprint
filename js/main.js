function HackNSlashSetup () {
	// Configure JawsJS
	jaws.init({
		width: 500,
		height: 600
	});

	// Load game assets.
	(function () {
		for(var tile in DEBUG_MAP.properties) {
			tile = DEBUG_MAP.properties[tile];

			if(tile.imageSrc) jaws.assets.add( tile.imageSrc );
		}
	})();

	// Start main Game Loop.
	jaws.start(
		// Start with this Game State
		new PlayState(),
		// Start-up options.
		{
			fps: 10
		},
		// Game State options.
		{
			map: DEBUG_MAP
		}
	);
}